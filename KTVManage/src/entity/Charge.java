package entity;

public class Charge {

	private int id;
	private int price;
	private int priveAdded;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getPriveAdded() {
		return priveAdded;
	}

	public void setPriveAdded(int priveAdded) {
		this.priveAdded = priveAdded;
	}
}
